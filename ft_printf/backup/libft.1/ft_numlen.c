#include "libft.h"

int ft_numlen(int n)
{
    int len;
    long nc;

    len = 0;
    nc = (long)n;
    if (nc <= 0)
    {
        len++;
        nc = -nc;
    }
    while (nc != 0)
    {
        len++;
        nc /= 10;
    }
    return (len);
}